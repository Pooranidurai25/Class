package week1.day2;

public class LearnBreak {

	public static void main(String[] args) {
// when the value is 5, come out ( terminate) of the loop
		
		
		for (int i = 0; i <= 10; i++) {
			// i =5
			if(i==5) {
				break;
			}
			System.out.println(i);
		}
		
	
		
		
		System.out.println(" Out of for loop");
	}

}
